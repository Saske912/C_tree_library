#include "tree.h"

int tree_compare_values( t_tree *existing, void *value, int mask )
{
	int 	val1;
	int 	val2;

	if (mask == LONG)
	{
		val1 = *((int *)existing->value);
		val2 = *((int *)value);
		if (val1 == val2)
			return (-1);
		return (val1 > val2);
	}
	else if (mask == STRING)
	{
		val1 = strcmp(existing->value, value);
		if (!val1)
			return (-1);
		else if (val1 > 0)
			return (1);
		else
			return (0);
	}
	error_exit("tree compare exception(unexpected type)\n");
	return (-1);
}