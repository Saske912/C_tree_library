#include "tree.h"

void	destroy_mutexes(t_mutexes *mutexes) {
	pthread_mutex_destroy(&mutexes->extract);
	pthread_mutex_destroy(&mutexes->top);
	pthread_mutex_destroy(&mutexes->left);
	pthread_mutex_destroy(&mutexes->right);
}