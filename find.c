#include "tree.h"

int		find(long number, t_tree *top)
{
	while (top && top->number != number)
	{
		if (top->number > number)
			top = top->left;
		else
			top = top->right;
	}
	if (top && top->number == number)
		return (1);
	else
		return (0);
}