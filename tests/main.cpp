#include "../tree.h"
#include <iostream>
#include <boost/chrono/system_clocks.hpp>
#include <fstream>

void 	appendFileToTree(t_tree	**tree) {
	std::ifstream ifs("/home/pfile/base_validator2/tree_lib/tests/10.txt");
	char buffer[100];

	while (ifs.good()) {
		bzero(buffer, 100);
		ifs.getline(buffer, 99);
		if (strlen(buffer) > 10 && tree_insert(tree, atol(buffer)) == -1) {
			std::cerr << "error: number already in tree." << std::endl;
		}
	}
}

int		main() {
	t_tree	*tree;

	using namespace boost::chrono;
	system_clock::time_point start = system_clock::now();
	tree = tree_file((char *)"/home/pfile/base_validator2/tree_lib/tests/container.txt");
	system_clock::time_point stop = system_clock::now();
	duration<double> sec = system_clock::now() - start;
	std::cout << "duration: " << sec.count() << " sec\n";
	std::cout << find(79913220583, tree) << std::endl;
	appendFileToTree(&tree);
	free_tree(&tree);
}